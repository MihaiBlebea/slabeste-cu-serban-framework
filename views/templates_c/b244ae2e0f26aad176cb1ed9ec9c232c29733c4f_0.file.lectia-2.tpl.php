                    <p>Fructele, de exemplu, sunt carbohidrati simpli. De aceea, nu sunt recomandate din abundenta atunci cand vrei sa slabesti, ci trebuie sa le privesti mai mult ca un desert.</p>
                    <p>Pe de alta parte, carbohidratii complecsi (naturali) sunt bogati in fibre si iti confera senzatia de satietate pentru mai mult timp.</p>
                    <p>Acestia se regasesc in alimenate precum psyllium, fulgi de ovaz, orezul brun, lintea, cartofii, fasolea si in legume (broccoli, telina, ciuperci, conopida etc.).</p>
                    <p>Abordarea mea cand vine vorba de carbohidrati este sa inveti sa ii castigi. Creierul tau si sistemul nervos central utilizeaza glucoza pentru a functiona, iar corpul tau ii foloseste ca sursa de energie atunci cand te antrenezi intens.</p>
                    <p>Asadar, consuma mai multi carbohidrati in zilele in care te antrenezi. Daca nu ai o activitate fizica intensa, limiteaza consumul de carbohidrati.</p>
