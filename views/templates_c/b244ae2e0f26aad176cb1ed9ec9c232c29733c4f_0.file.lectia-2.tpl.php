                <div class="text-section">
                    <p>Circula pe internet mitul ca daca mananci carbohidrati te ingrasi, insa nu tot carbohidratii sunt la fel.</p>
                    <p>Carbohidratii buni, in cantitati moderate nu te ingrasa, ba chiar te ajuta sa slabesti, daca stii in ce cantitate si cand sa ii mananci.</p>
                    <p>Daca obiectivul tau este sa scapi de grasime, atunci regula prinvind carbohidratii este ca trebuie sa ii castigi mai intai.</p>
                    <p>Carbohidratii sunt principala sursa de energie a corpului tau. Atunci cand mananci carbohidrati, pancreasul elibereaza insulina – un hormon care regleaza nivelul de zahar din sange.</p>
                    <p>Insulina depoziteaza carbohidratii in muschi sau in ficat sub forma de glicogen. Daca rezerverele de glicogen sunt pline, carbohidratii sunt depozitati sub forma de grasime.</p>
                    <p>Exista doua tipuri de carbohidrati: simpli si complecsi.Carbohidratii simpli (procesati)sunt absorbiti foarte rapid de corpul tau.</p>
                    <p>Cand mananci carbohidrati simpli nivelul zaharului din sange creste brusc, dar scade la fel de repede.Din aceasta cauza, ei provoaca cresterea nivelului de insulina si, in consecinta, depozitarea carbohidratilor in exces sub forma de grasime.</p>
